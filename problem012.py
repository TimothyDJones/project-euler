from math import sqrt

def main():
    MAX_DIVISORS = 500
    
    for n in range(1, 100_000_000):
        tri_num = sum([i for i in range(1, n)])
        d = divisors(tri_num)
        if len(d) > MAX_DIVISORS:
            break
                    
    print(tri_num)
    
def divisors(n: int):
    d = list()
    ceil = int(sqrt(n)) + 1
    
    for i in range(1, ceil):
        if n % i == 0:
            d.append(i)
            
            if i != n // i:
                d.append(n // i)
                
    return d

if __name__ == "__main__":
    main()
